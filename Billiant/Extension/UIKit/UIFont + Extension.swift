import UIKit

// MARK: - Extension

extension UIFont {
    // MARK: - Methods
    
    static func getGeezaProBold20() -> UIFont? {
        return UIFont.init(name: "Geeza Pro Bold", size: 20)
    }
    
    static func getGeezaProRegular14() -> UIFont? {
        return UIFont.init(name: "Geeza Pro Regular", size: 14)
    }
}
